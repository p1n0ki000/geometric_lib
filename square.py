
def area(a):
    '''
    Calculates the area of a square from the length of the side.

    argument:
        a (int): length of the side

    return value:
        (int): area of a square

    example:
        >> area(2);
        4
    '''
    return a * a


def perimeter(a):
    '''
    Calculates the perimeter of a square from the length of the side.

    argument:
        a (int): length of the side

    return value:
        (int): perimeter of a square

    example:
        >> perimeter(2);
        8
    '''
    return 4 * a